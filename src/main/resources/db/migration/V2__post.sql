-- 5. Categories
CREATE TABLE IF NOT EXISTS categories
(
    id   BIGINT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL
);
CREATE TABLE post_types
(
    id   BIGINT PRIMARY KEY AUTO_INCREMENT,
    code VARCHAR(50) UNIQUE NOT NULL,
    name VARCHAR(100)       NOT NULL
);

-- 6. Post
CREATE TABLE posts
(
    id              BIGINT PRIMARY KEY AUTO_INCREMENT,
    user_id         BIGINT    NOT NULL,
    title           VARCHAR(255),
    description     TEXT,
    price           DECIMAL(12, 2),
    area            FLOAT,
    address         TEXT,
    type_id         BIGINT,
    is_vip          BIGINT,
    vip_expiry_date TIMESTAMP NULL,
    status          ENUM ('PENDING', 'APPROVED', 'EXPIRED', 'REJECTED', 'PAYING') DEFAULT 'PENDING',
    created_at      TIMESTAMP                                           DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMP                                           DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    name_contact VARCHAR(255),
    phone_contact  VARCHAR(255),


    FOREIGN KEY (user_id) REFERENCES users (id),
    FOREIGN KEY (type_id) REFERENCES post_types (id)
);

-- Post_Categories
CREATE TABLE post_category
(
    post_id     BIGINT,
    category_id BIGINT,
    PRIMARY KEY (post_id, category_id),
    FOREIGN KEY (post_id) REFERENCES posts (id) ON DELETE CASCADE,
    FOREIGN KEY (category_id) REFERENCES categories (id) ON DELETE CASCADE
);

-- 7. post Images
CREATE TABLE post_images
(
    id           BIGINT PRIMARY KEY AUTO_INCREMENT,
    post_id      BIGINT NOT NULL,
    image_url    TEXT,
    is_thumbnail BOOLEAN DEFAULT FALSE,
    FOREIGN KEY (post_id) REFERENCES posts (id)
);

-- city
CREATE TABLE cities
(
    id   BIGINT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(100) NOT NULL UNIQUE -- VD: "Hà Nội"
);

-- districts
CREATE TABLE districts
(
    id      BIGINT PRIMARY KEY AUTO_INCREMENT,
    name    VARCHAR(100) NOT NULL,
    city_id BIGINT       NOT NULL,
    FOREIGN KEY (city_id) REFERENCES cities (id),
    UNIQUE (name, city_id)
);

-- wards
CREATE TABLE wards
(
    id          BIGINT PRIMARY KEY AUTO_INCREMENT,
    name        VARCHAR(100) NOT NULL,
    district_id BIGINT       NOT NULL,
    FOREIGN KEY (district_id) REFERENCES districts (id),
    UNIQUE (name, district_id)
);

--  posts_addresses
CREATE TABLE posts_addresses
(
    id             BIGINT PRIMARY KEY AUTO_INCREMENT,
    post_id        BIGINT NOT NULL,
    detail_address VARCHAR(255), -- VD: "60 ngõ 68 Đường Cầu Giấy"
    ward_id        BIGINT NOT NULL,
    latitude       DOUBLE,       -- optional (cho map)
    longitude      DOUBLE,       -- optional (cho map)
    FOREIGN KEY (post_id) REFERENCES posts (id),
    FOREIGN KEY (ward_id) REFERENCES wards (id)
);
